char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] -= 32;
	i++;
	while (str[i])
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += 32;
		if ((str[i - 1] == ' ' || str[i - 1] == '\n' || str[i - 1] == '\f'
		|| str[i - 1] == '\r' || str[i - 1] == '\t' || str[i - 1] == '\v')
		&& (str[i] >= 'a' && str[i] <= 'z'))
			str[i] -= 32;
		i++;
	}
	return (str);
}
