char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	char			*s;
	unsigned int	i;

	s = dest;
	while (*dest != '\0')
		dest++;
	i = 0;
	while (i < nb && *src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
		i++;
	}
	*dest = '\0';
	return (s);
}
